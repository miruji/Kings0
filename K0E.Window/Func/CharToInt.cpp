#include "CharToInt.h"

size_t CharToInt(char C)
{
	// ... 
	if (C == '0')
		return 0;
	else
	if (C == '1')
		return 1;
	else
	if (C == '2')
		return 2;
	else
	if (C == '3')
		return 3;
	else
	if (C == '4')
		return 4;
	else
	if (C == '5')
		return 5;
	else
	if (C == '6')
		return 6;
	else
	if (C == '7')
		return 7;
	else
	if (C == '8')
		return 8;
	else
	if (C == '9')
		return 9;
}